    function validateNameInput(input) {
        if (/[^A-Za-z\s]/.test(input.value)) {
            input.value = input.value.replace(/[^A-Za-z\s]/g, '');
            input.setCustomValidity('Only letters and spaces are allowed in the name field.');
            input.reportValidity();
        } else {
            input.setCustomValidity('');
        }
    }

    function validatePhoneInput(input) {
        if (/[^\d\s\+\-\(\)]/.test(input.value)) {
            input.value = input.value.replace(/[^\d\s\+\-\(\)]/g, '');
            input.setCustomValidity('Only numbers and standard symbols (+, -) are allowed in the phone field.');
            input.reportValidity();
        } else {
            input.setCustomValidity('');
        }
    }

    function handleGoogleFormSubmit(form, event) {
        event.preventDefault();
        
        // Basic HTML5 Validation
        if (!form.checkValidity()) {
            form.reportValidity();
            return false;
        }

        var btn = form.querySelector('button[type="submit"]');
        if(btn.disabled) return false;

        var originalText = btn.innerHTML;
        btn.innerHTML = "<span class='fas fa-spinner fa-spin'></span> Sending...";
        btn.disabled = true;

        var data = new FormData(form);
        fetch(form.action, {
            method: form.method,
            body: data,
            mode: 'no-cors'
        }).then(function() {
            window.location.href = "thank-you.php";
        }).catch(function(err) {
            console.error(err);
            alert("Error sending request. Please try again.");
            btn.innerHTML = originalText;
            btn.disabled = false;
        });

        return false;
    }
